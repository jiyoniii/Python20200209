list5 = list(range(0, 5, 1))
print(type(list5), list5)
############################


# 리스트 요소 출력
# 리스트의 시작은 0번 부터
# 리스트의 Read : [ ] 선택 연산자를 사용한다.
############################
array = [1, 1.1, '문자', True, [0.1], {"a": 1, "1": "ba"}, None]
print(type(array[5]), array[5])

############################
# 리스트 출력
############################
print("array", array)
############################
# 리스트 슬라이싱 ->선택연산자 사용하는 방법.
############################
print(" ", array[0])
print(" ", array[1:3])
print(" ", array[-1])
print(" ", array[-3])
############################
# 리스트 요소 대입
# 리스트의 0번 값을 문자열 "변경" 값으로 바꾸시오.
############################
print(array[0])
array[0] = "변경"
print(array[0])

############################
# 리스트 요소 추가 : C. create ==> 추가와 삽입
# 추가 : list의 마지막에 요소를 넣는다. --> .append()
# 삽입 : list 의 중간에 요소를  넣는다. --> .insert()
############################
array.append("추가")
print("array", array)
array.insert(0, "삽입")
print("array", array)
############################
# 리스트 요소 삭제 : D. delete
# 메서드 방식 -> pop( ) 메서드. 추천
# 연산자 방식 --> del( ) 메서드. 비추천
############################
array.pop(0)
print("0번을 삭제함==>>", array)
del array[0]
print("0번을 또 삭제함==>", array)

# 리스트 열거(무조건 for 사용)
############################
for i in array:
    print("리스트 열거==>>", i)

